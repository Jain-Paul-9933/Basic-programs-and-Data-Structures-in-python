choice = int(input("Enter the corresponding day number  "))
if choice == 1:
    print("Sunday")
elif choice == 2:
    print("Monday")
elif choice == 3:
    print("Tuesday")
elif choice == 4:
    print("Wednesday")
elif choice == 5:
    print("Thursday")
elif choice == 6:
    print("Friday")
elif choice == 7:
    print("Saturday")
else:
    print("Inavalid Input")
